/**
 * Telegram Delivery Service
 *
 * Formats and sends 3-day analytics reports to a Telegram chat.
 * Uses the Telegram Bot API directly — no extra libraries needed.
 *
 * Message format uses Telegram MarkdownV2 so it renders cleanly
 * in the chat with bold headers and code blocks.
 */

import type { Report, ReportStats, Site } from "@/types";

const BOT_TOKEN = process.env.TELEGRAM_BOT_TOKEN!;
const TELEGRAM_API = `https://api.telegram.org/bot${BOT_TOKEN}`;

/** Send a formatted analytics report to a Telegram chat */
export async function sendReportToTelegram(
  chatId: string,
  site: Site,
  report: Report
): Promise<void> {
  const text = formatReportMessage(site, report);
  await sendMessage(chatId, text);
}

/** Send an arbitrary message to a Telegram chat */
export async function sendMessage(
  chatId: string,
  text: string,
  parseMode: "MarkdownV2" | "HTML" | "Markdown" = "HTML"
): Promise<void> {
  const res = await fetch(`${TELEGRAM_API}/sendMessage`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      chat_id: chatId,
      text,
      parse_mode: parseMode,
      disable_web_page_preview: true,
    }),
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Telegram API error: ${res.status} — ${err}`);
  }
}

// ─────────────────────────────────────────────
// Message formatter
// ─────────────────────────────────────────────

function formatReportMessage(site: Site, report: Report): string {
  const stats = report.raw_stats;
  const periodStart = new Date(report.period_start);
  const periodEnd = new Date(report.period_end);
  const period = `${formatDate(periodStart)} – ${formatDate(periodEnd)}`;

  // Build lines — escape special chars for MarkdownV2
  const lines: string[] = [
    `<b>📊 ${esc(site.name)}</b> 3-Day Report`,
    `<i>${esc(period)}</i>`,
    ``,
    `<b>Core Metrics</b>`,
    `👁 Pageviews: <b>${n(stats.total_pageviews)}</b>`,
    `👤 Unique visitors: <b>${n(stats.unique_visitors)}</b>`,
    `🔁 Sessions: <b>${n(stats.sessions)}</b>`,
    `↩️ Bounce rate: <b>${stats.bounce_rate.toFixed(1)}%</b>`,
    `⏱ Avg session: <b>${formatDuration(stats.avg_session_duration_s)}</b>`,
    ``,
  ];

  // Top pages
  if (stats.top_pages.length > 0) {
    lines.push(`*Top Pages*`);
    stats.top_pages.slice(0, 5).forEach((p, i) => {
      lines.push(`${i + 1}\\. ${esc(p.url)} — ${n(p.views)} views`);
    });
    lines.push(``);
  }

  // Top traffic sources
  if (stats.top_referrers.length > 0) {
    lines.push(`<b>Traffic Sources</b>`);
    stats.top_referrers.slice(0, 5).forEach((r) => {
      lines.push(`• ${esc(r.source)} — ${n(r.visits)} visits`);
    });
    lines.push(``);
  }

  // Top countries
  if (stats.top_countries.length > 0) {
    lines.push(`<b>Top Countries</b>`);
    stats.top_countries.slice(0, 3).forEach((c) => {
      lines.push(`• ${esc(c.country)} — ${n(c.visits)} visits`);
    });
    lines.push(``);
  }

  // Custom events
  if (stats.top_events.length > 0) {
    lines.push(`<b>Custom Events</b>`);
    stats.top_events.slice(0, 5).forEach((e) => {
      lines.push(`• ${esc(e.name)}: ${n(e.count)}x`);
    });
    lines.push(``);
  }

  // AI Summary
  lines.push(`<b>📝 AI Summary</b>`);
  lines.push(esc(report.summary));
  lines.push(``);

  // Growth suggestions
  lines.push(`<b>💡 Growth Suggestions</b>`);
  lines.push(esc(report.suggestions));
  lines.push(``);

  lines.push(`<i>Generated by Brieflytics · ${esc(new Date().toLocaleDateString("en-GB"))}</i>`);

  return lines.join("<br/>\n");
}

// ─────────────────────────────────────────────
// Utilities
// ─────────────────────────────────────────────

/** Escape special characters for Telegram MarkdownV2 */
function esc(text: string): string {
  return text.replace(/[_*[\]()~`>#+\-=|{}.!\\]/g, "\\$&");
}

/** Format a number with thousands separator */
function n(num: number): string {
  return num.toLocaleString("en-US");
}

function formatDate(d: Date): string {
  return d.toLocaleDateString("en-GB", {
    day: "numeric",
    month: "short",
  });
}

function formatDuration(seconds: number): string {
  if (seconds < 60) return `${Math.round(seconds)}s`;
  const m = Math.floor(seconds / 60);
  const s = Math.round(seconds % 60);
  return `${m}m ${s}s`;
}
